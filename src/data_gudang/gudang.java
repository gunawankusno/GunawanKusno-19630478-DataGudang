    private void tampil(){
        int row=jTable1.getRowCount();
        for(int x=0; x<row;x++){
            model.removeRow(0);
        }
        try{
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/data_gudang","root","");
            ResultSet rs=con.createStatement().executeQuery("select * from gudang");
            while(rs.next()){
                String data[]={rs.getString(1),rs.getString(2),rs.getString(3)};
                model.addRow(data);
            }
        }catch (SQLException ex){
            Logger.getLogger(gudang.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
    private void reset(){
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
    }
    
